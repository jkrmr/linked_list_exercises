module LinkedList
  # models a single node in a linear linked list
  class Node; end

  # implements the linear linked list adt
  class List; end

  # implements the stack adt using a linear linked list
  class Stack; end

  # reverses a linked list without mutation
  class Reverser; end

  # reverses a linked list in place
  class DestructiveReverser; end
end

